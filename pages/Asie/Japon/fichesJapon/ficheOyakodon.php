<h1>
	Oyakodon: riz au poulet</h1>
<div class="uk-section">
	<div class="uk-container centre">
		<p>
			Pour 2 personnes, 20 minutes de préparation, 20 minutes de cuisson
		</p>
		<div class="uk-panel">
			<img class="uk-align-center uk-margin-remove-adjacent" data-src="<?php echo $cheminImg; ?>i75518-oyakodon-bol-de-riz-et-poulet-aux-oeufs-japonais.jpg"
			 width="305" alt="Un bol d'oyakodon" uk-img>
			<h4>Ingrédients: </h4>
			<p>
				100 g de filet de poulet</br>
				1 oignon</br>
				5 cm de poireau (partie verte)</br>
				2 oeufs</br>
				1 verre de riz rond</br>
				2 + 1/4 de verres d'eau</br>
				2 c. à café de sucre</br>
				2 c. à soupe de sauce soja</br>
				1/2 cuillère à café de dashi</br>
				De l'huile de tournesol
			</p>
		</div>
		<h4>Préparation</h4>
		<ul class="uk-list uk-list-divider">
			<li>1: Faites chauffer un peu d'huile dans une sauteuse.</li>
			<li>2: Versez le riz et mélangez jusqu'à ce qu'il devienne translucide.</li>
			<li>3: Ajoutez un peu d'eau et laissez cuire jusqu'à son évaporation.</li>
			<li>4: Répétez l'opération jusqu'à épuiser de 2 verres d'eau. Le riz doit être cuit mais légèrement collant.</li>
			<li>5: Réservez le riz cuit au chaud.</li>
			<li>6: Epluchez et hachez l'oignon.</li>
			<li>7: Couper le poireau en tronçons.</li>
			<li>8: Faites chauffer 1/4 de verre d'eau dans une poêle avec le bouillon.</li>
			<li>9: Ajouter la moitié de l'oignon et du poireau et laissez cuire jusqu'à ce qu'ils soient ramollis.</li>
			<li>10: Ajoutez la moitié du poulet, 1 c. à café de sucre et 1 c.à soupe de sauce soja.</li>
			<li>11: Cassez un oeuf dans un petit bol et mélangez.</li>
			<li>12: Quand le poulet a changé de couleur, versez l'oeuf en suivant le contour du bol et couvrez.</li>
			<li>13: Quand le contour de la préparation est cuit, éteignez le feu en laissant à couvert quelques minutes.</li>
			<li>14: Servez la moitié du riz dans un grand bol et faites-y glisser la préparation au poulet..</li>
			<li>15: Répétez l'opération de la préparation du poulet pour le deuxième bol.</li>
			<li>16: Dégustez pendant que c'est encore chaud.</li>
		</ul>
