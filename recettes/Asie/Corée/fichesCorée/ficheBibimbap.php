                <h4>Préparation</h4>
                <ul class="uk-list uk-list-divider">
                    <li>1: Dans un récipient, mélangez les viandes hachées avec tous les ingrédients de la marinade puis réservez
                        au frais.</li>
                    <li>2: Coupez, épépinez et taillez le concombre en lamelles. Pelez et coupez en petits bâtonnets la carotte.
                        Pelez et émincez finement l'oignon. Rincez les épinards et les pousses de soja. Pelez et hachez finement
                        les gousses d'ail.</li>
                    <li>3: Dans une casserole remplie d'eau bouillante, faites cuire le riz selon les indications du paquet.</li>
                    <li>4: Pendant ce temps, faites revenir les légumes (exceptés l'ail et l'oignon) les uns après les autres
                        dans des poêles bien chaudes avec un filet d'huile de sésame. Ajoutez à chacun un peu d'ail et d'oignon
                        et une pincée de sucre blanc. Réservez hors du feu.</li>
                    <li>5: Dans une autre poêle avec un filet d'huile, faites revenir les viandes avec la marinade. Retirez et
                        réservez hors du feu.</li>
                    <li>6: Dans un bol, préparez la sauce en mélangeant tous les ingrédients.</li>
                    <li>7: Quand le riz est cuit, égouttez-le et répartissez-le dans les bols. Versez dans chaque bol 2 cuillères
                        à soupe de sauce et recouvrez d'un jaune d'oeuf entier, au centre, puis répartissez tout autour le
                        mélange de viande et les légumes poêlés, sans les mélanger.</li>
                    <li>8: Saupoudrez de graines de sésame et servez sans attendre.</li>
                </ul>
