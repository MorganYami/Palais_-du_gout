                <h4>Préparation</h4>
                <ul class="uk-list uk-list-divider">
                    <li>1: Faire tremper les champignons dans de l'eau chaude 20 mn. Les couper en lanières de 5 mm et les remettre
                        dans l'eau en attendant.</li>
                    <li>2: Emincer les carottes en bâtonnets de 5 mm de côté et de 5 cm de long.</li>
                    <li>3: Emincer l'oignon comme la carotte.</li>
                    <li> Préparer la pâte à beignets: </li>
                    <li>4:remplir un verre de 20 cl de farine, la verser dans un saladier.</li>
                    <li>5: Casser l'oeuf dans le verre et compléter avec de l'eau. Mélanger le contenu du verre, puis verser
                        progressivement sur la farine en remuant avec des baguettes, une fourchette ou un fouet pour les
                        moins patients.</li>
                    <li>6: Mettre tous les ingrédients dans la pâte (y compris les lamelles d'encornet cru et les crevettes),
                        remuer. Former des tas entre deux cuillères à soupe, égoutter un peu et faire glisser dans un bain
                        de friture chaud. Cuire 5 mn et égoutter sur du papier absorbant.</li>
                    <li>7: Pour la sauce, on râpe le radis (ou le daïkon) dans un bol et on recouvre de sauce Tempura. On laisse
                        macérer de 30 min à 1 heure.</li>
                    <li>8: Servir chaud.</li>
